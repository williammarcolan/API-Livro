<p>Metodo: <b>GET</b></p>
<p>Rota: <b>/api/:id</b></p>
<p>retorna: <b>livro com id especificado</b></p>

</br>

<p>Metodo: <b>GET</b></p>
<p>Rota: <b>/api/</b></p>
<p>retorna: <b>todos os livros</b></p>

</br>

<p>Metodo: <b>POST</b></p>
<p>Rota: <b>/api/</b></p>
<p>Body: <b>livro em json (sem id)</b></p>
<p>retorna: <b>livro em json (com id)</b></p>

</br>

<p>Metodo: <b>PUT</b></p>
<p>Rota: <b>/api/:id</b></p>
<p>Body: <b>livro alterado em json (sem id)</b></p>
<p>retorna: <b>livro alterado livro em json (com id)</b></p>

</br>

<p>Metodo: <b>DELETE</b></p>
<p>Rota: <b>/api/:id</b></p>
<p>retorna: <b>livro deletado</b></p>